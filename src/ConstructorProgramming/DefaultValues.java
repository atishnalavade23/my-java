package ConstructorProgramming;

public class DefaultValues {
        char ch;
        int a;
        short sh;
        long l;
        boolean bool;
        double d;
        float f;



    public static void main(String[] args) {
        DefaultValues obj = new DefaultValues();
        System.out.println("The value of Char is :"+obj.ch);
        System.out.println("The value of Int is :"+obj.a);
        System.out.println("The value of Short is :"+obj.sh);
        System.out.println("The value of Long is :"+obj.l);
        System.out.println("The value of Float is :"+obj.f);
        System.out.println("The value of Double is :"+obj.d);
        System.out.println("The value of Boolean is :"+obj.bool);

    }
}
